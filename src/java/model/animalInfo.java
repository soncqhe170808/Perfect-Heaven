package model;

import java.sql.Date;

/**
 *
 * @author FPT
 */
public class animalInfo {
    int id, price;
    float weight;
    String type, description, imgPath;
    char gender; 
    Date dob;
    boolean sold;
    public animalInfo(int id, int price, float weight, String type, String description, String imgPath, char gender, Date dob, boolean sold) {
        this.id = id;
        this.price = price;
        this.weight = weight;
        this.type = type;
        this.description = description;
        this.imgPath = imgPath;
        this.gender = gender;
        this.dob = dob;
        this.sold = sold;
    }

    

    public animalInfo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImgPath() {
        return imgPath;
    }

    public void setImgPath(String imgPath) {
        this.imgPath = imgPath;
    }

    public char getGender() {
        return gender;
    }

    public void setGender(char gender) {
        this.gender = gender;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public boolean isSold() {
        return sold;
    }

    public void setSold(boolean sold) {
        this.sold = sold;
    }
    
    
    
}
